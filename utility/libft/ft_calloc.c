#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*str;

	str = (void *)malloc(count * size);
	if (!str || (SIZE_MAX == size && SIZE_MAX == count))
		return (0);
	ft_bzero(str, size * count);
	return (str);
}
